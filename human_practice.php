						<h1 id = "polprac">Policy and Practices</h1>
						<p class = "col-sm-12">Without effective communication and collaboration between scientists and the general public, all of the efforts we make in the laboratory are potentially useless.  In an age where there is so much distrust and misinformation about scientific research, we must double our efforts to educate, collaborate and listen to what the general public has to say about the state of scientific research.</p>
						<p class = "col-sm-12">
							Throughout the policy and practices portion of the iGEM project, we have gained a tremendous about of insight into the public's knowledge and concerns about synthetic biology.  We have sought to educate as many people as possible about our project, synthetic biology in general and the amazing potential of re-engineered genetic engineering.
						</p>
						<p class = "col-sm-8">
							Our policy and practices project is divided into four sections: "Research", "Change", "Outreach", and "Entertainment". In the "Research" and "Change" sections we gathered information and data from the public and from experts in science, medicine and policy analysis so that we could inform ourselves about ALL aspects of our project not just experiments in the lab.  Since our project is a health and medicine project, we then took this research and created a policy brief outlining changes that we would like to see in the pharmaceutical and biopharmaceutical industry.  This innovative policy brief was then sent to government agencies and some of the most influential people in the world in hopes that they would help us make a lasting change in the world.
							The "Outreach" and "Entertainment" sections show all of the programs, activities and meet-ups that we have had with various student and public groups. 
